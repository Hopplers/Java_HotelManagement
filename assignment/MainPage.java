    private void btnSubmit2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmit2ActionPerformed
        
        //Creates a empty error then adds the error when occur
        Message = "";
        if (!validate.alphabetValidation(txtName.getText())) Message += "Please Input Alphabets Only in Name\n";
        if (!validate.numberValidation(txtContactNumber.getText())) Message += "Please Input Numbers Only in Contact Number\n";
        if (!validate.emailValidation(txtEmail.getText())) Message += "Please Input Correct Email in Email\n";
        if (!validate.passportValidation(txtICPassport.getText())) Message += "Please Input Alphanumeric Only in IC/Passport\n";
        if (!radForeigner.isSelected()&&!radMalaysian.isSelected()) Message += "Please Select Customer's Nationality\n";
        
        if (!Message.equals("")){
            JOptionPane.showMessageDialog(null, Message, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //Read lines in textfile
        lines = 0;
        try (BufferedReader reader = new BufferedReader(new FileReader("C:\\Users\\Acer\\OneDrive\\Documents\\NetBeansProjects\\Assignment\\src\\assignment\\data\\Booking.txt"))) {
            while (reader.readLine() != null) lines++;
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error reading file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        if (lines == 1){
            bID = "BK0001";
        }
        else{
            //Algo to create bookingid when textfile not empty
            String latestKey = null;
            try{
                BufferedReader br = new BufferedReader(new FileReader("C:\\Users\\Acer\\OneDrive\\Documents\\NetBeansProjects\\Assignment\\src\\assignment\\data\\Booking.txt"));

                for (int i = 0; i < lines - 1; i++){
                    br.readLine();
                }
                String[] row = br.readLine().split(",");
                latestKey = row[0];
                
            }catch(IOException er) {
                JOptionPane.showMessageDialog(null, "Error reading file: " + er.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
            
            Pattern pattern = Pattern.compile("\\d+");
            Matcher matcher = pattern.matcher(latestKey);
            if (matcher.find()) {
                int numericValue = Integer.parseInt(matcher.group());
                numericValue++;
                bID = "BK" + String.format("%04d", numericValue);
            }
        }
        
        
        cID = formatter.format(dateCIDate2.getDate());
        cOD = formatter.format(dateCODate2.getDate());
        rID = lblRoomID.getText();
        name = txtName.getText();
        c = txtContactNumber.getText();
        e = txtEmail.getText();
        iCP = txtICPassport.getText();

        if (radMalaysian.isSelected())
            nat = radMalaysian.getText();
        else if (radForeigner.isSelected())
            nat = radForeigner.getText();

        //save into textfile
        SaveData customer = new SaveData(bID, cID, cOD, rID, name, c, e, iCP, nat);
        String result = customer.saveData();
        if(result == "Success")
            JOptionPane.showMessageDialog(null,"Registration completed.","Success",JOptionPane.INFORMATION_MESSAGE);
        else
            JOptionPane.showMessageDialog(null, "Could not write into file due to " + result,"Error",JOptionPane.ERROR_MESSAGE);
        
        lblName.setText(name);
        lblContactNumber.setText(c);
        lblEmail.setText(e);
        lblICPassport.setText(iCP);
        lblCID.setText(cID);
        lblCOD.setText(cOD);
        lblRID.setText(lblRoomID.getText());
        lblBookingID.setText(bID); 
        lblNationality.setText(nat);
        //leads to conclusion for booking
        JTabbedPane.setSelectedIndex(4);
        // TODO add your handling code here:
    }//GEN-LAST:event_btnSubmit2ActionPerformed

