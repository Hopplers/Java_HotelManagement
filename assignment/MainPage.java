    private void btnSubmit4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmit4ActionPerformed

        //Creates a empty error then adds the error when occur
        Message = "";
        if (!validate.alphabetValidation(txtName2.getText())) Message += "Please Input Alphabets Only in Name\n";
        if (!validate.numberValidation(txtContactNumber2.getText())) Message += "Please Input Numbers Only in Contact Number\n";
        if (!validate.emailValidation(txtEmail2.getText())) Message += "Please Input Correct Email in Email\n";
        if (!validate.passportValidation(txtICPassport2.getText())) Message += "Please Input Alphanumeric Only in IC/Passport\n";
        if (!radForeigner2.isSelected()&&!radMalaysian2.isSelected()) Message += "Please Select Customer's Nationality\n";
        
        if (!Message.equals("")){
            JOptionPane.showMessageDialog(null, Message, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        editedData = new String[6];
        
        if (radMalaysian2.isSelected()){
            nat = "Malaysian";
        }else{
            nat = "Foreigner";
        }
        
        editedData[0] = lblBookingID3.getText();
        editedData[1] = txtName2.getText();
        editedData[2] = txtContactNumber2.getText();
        editedData[3] = txtEmail2.getText();
        editedData[4] = txtICPassport2.getText();
        editedData[5] = nat;
        
        change.editRowData("C:\\Users\\Acer\\OneDrive\\Documents\\NetBeansProjects\\Assignment\\src\\assignment\\data\\Booking.txt", editedData);
        JOptionPane.showMessageDialog(null, "Data Updated!", "Updated", JOptionPane.INFORMATION_MESSAGE);
        
        //replace shown
        lblName.setText(editedData[1]);
        lblContactNumber.setText(editedData[2]);
        lblEmail.setText(editedData[3]);
        lblICPassport.setText(editedData[4]);
        lblNationality.setText(editedData[5]);
        ////back to conclusion of booking
        JTabbedPane.setSelectedIndex(4);
                
        // TODO add your handling code here:
    }//GEN-LAST:event_btnSubmit4ActionPerformed

